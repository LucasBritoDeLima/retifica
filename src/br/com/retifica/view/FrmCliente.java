    private void initComponents2() {
        try {
            CNPJMask = new MaskFormatter("##.###.###/####-##");
            CPFMask = new MaskFormatter("###.###.###-##");
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        try {
            PhoneMask = new MaskFormatter("(##) # ####-####");
            FixoMask = new MaskFormatter("(##) ####-####");
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

        radioCelular.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    txtPhone2.setValue(null);
                    txtPhone2.setFormatterFactory(new DefaultFormatterFactory(PhoneMask));
                }
            }
        });

        radioFixo.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    txtPhone2.setValue(null);
                    txtPhone2.setFormatterFactory(new DefaultFormatterFactory(FixoMask));
                }
            }
        });

        radioCPF.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    txtCPFCNPJ.setValue(null);
                    txtCPFCNPJ.setFormatterFactory(new DefaultFormatterFactory(CPFMask));
                }
            }
        });

        radioCNPJ.addItemListener(new ItemListener() {
            @Override
            public void itemStateChanged(ItemEvent e) {
                if (e.getStateChange() == ItemEvent.SELECTED) {
                    txtCPFCNPJ.setValue(null);
                    txtCPFCNPJ.setFormatterFactory(new DefaultFormatterFactory(CNPJMask));
                }
            }
        });
    }

