    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtName.getText().isEmpty()
                || txtPhone.getText().isEmpty()
                || txtStreet.getText().isEmpty()
                || txtNumber.getText().isEmpty()
                || txtDistrict.getText().isEmpty()
                || txtCep.getText().isEmpty()
                || txtState.getText().isEmpty()
                || txtCidade.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Preencha os campos em branco");
        } else {

            new Thread() {
                int i = 0;

                @Override
                public void run() {
                    while (i < 100) {
                        i += 1;

                        lblOp.setText(String.valueOf(i) + "%");
                        proClient.setValue(i);
                        try {
                            sleep(10);
                        } catch (InterruptedException e) {
                            System.out.println(e);
                        }
                    }
                    Cliente cliente = new Cliente();
                    ClienteDAO dao = new ClienteDAO();

                    cliente.setFullName(txtName.getText());
                    cliente.setCpf(txtCPFCNPJ.getText());
                    cliente.setRg(txtRGIE.getText());
                    cliente.setTel(txtPhone.getText());
                    cliente.setTel2(txtPhone2.getText());
                    cliente.setStreet(txtStreet.getText());
                    cliente.setNumber(txtNumber.getText());
                    cliente.setDistrict(txtDistrict.getText());
                    cliente.setZipCode(txtCep.getText());
                    cliente.setState(txtState.getText());
                    cliente.setCity(txtCidade.getText());
                    cliente.setTbRetificaIdRetifica(Integer.parseInt(lblRetifica.getText()));

                    dao.insertClient(cliente);
                    clearField();

                }
            }.start();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void tableClientMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableClientMouseClicked
        // Pega os dados da tabela
        jTabbedPane1.setSelectedIndex(0);

        txtIDClient.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 0).toString());
        txtName.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 1).toString());
        String document = tableClient.getValueAt(tableClient.getSelectedRow(), 2).toString();
        if (document.length() == 14) {
            radioCPF.setSelected(true);
            txtCPFCNPJ.setText(document);
        } else {
            radioCNPJ.setSelected(true);
            txtCPFCNPJ.setText(document);
        }
        txtRGIE.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 3).toString());
        txtPhone.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 4).toString());
        String phone = tableClient.getValueAt(tableClient.getSelectedRow(), 5).toString();
        if (phone.length() == 14) {
            radioFixo.setSelected(true);
            txtPhone2.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 5).toString());
        } else {
            radioCelular.setSelected(true);
            txtPhone2.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 5).toString());
        }
        txtStreet.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 6).toString());
        txtNumber.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 7).toString());
        txtDistrict.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 8).toString());
        txtCep.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 9).toString());
        txtCidade.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 11).toString());
        txtState.setText(tableClient.getValueAt(tableClient.getSelectedRow(), 10).toString());

        btnSave.setEnabled(false);
        btnEdit.setEnabled(true);
        btnDelete.setEnabled(true);
    }//GEN-LAST:event_tableClientMouseClicked

